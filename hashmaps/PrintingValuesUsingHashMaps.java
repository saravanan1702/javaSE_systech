package hashmaps;

import java.util.HashMap;

public class PrintingValuesUsingHashMaps {
	public static void main(String[] args) {
		HashMap<Integer, String> a = new HashMap<>();
		a.put(10, "saravanan");
		a.put(2, "Santhosh");
		a.put(3, "Dhara");
		a.put(6, "Sara");
		a.put(4, "Dora");
        System.out.println("Hashmap "+a);
	}
}
